template <class T>
bool lt(T value, T threshold){
    return value < threshold;
}
template <class T>
bool leq(T value, T threshold){
    return value <= threshold;
}
template <class T>
bool gt(T value, T threshold){
    return value > threshold;
}
template <class T>
bool geq(T value, T threshold){
    return value >= threshold;
}