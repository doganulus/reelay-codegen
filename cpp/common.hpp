template <class V, class T>
bool lt(V value, T threshold){
    return value < threshold;
}
template <class V, class T>
bool leq(V value, T threshold){
    return value <= threshold;
}
template <class V, class T>
bool gt(V value, T threshold){
    return value > threshold;
}
template <class V, class T>
bool geq(V value, T threshold){
    return value >= threshold;
}